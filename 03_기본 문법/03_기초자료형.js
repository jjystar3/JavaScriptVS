/* 기초자료형 */

// String (문자열)
// 문자열은 "" '' 로 표기할 것
const myName = "둘리";
const email = 'user@naver.com';

// 문자열 안에 변수를 포함할 때는 백틱과 ${변수}을 사용할 것
const hello = `Hello ${myName}`;
console.log(hello);

// Number
const number = 123;
const opacity = 0.57;

// Boolean
let checked = true;
let isShow = false;

// undefined
// 값을 대입하지 않으면 undefined가 자동으로 할당됨
let undef;
console.log(undef);

// null
// undefined와 달리 개발자가 직접 값을 대입해야 함
let name = null;
console.log(name);
